def generate_exclusion_zones():
    """生成禁区数据，使用福州真实水域/林地/机场坐标"""
    zones = []
    seen_names = set()

    type_display_map = {
        "water": "水域/河道",
        "forest": "林地/保护区",
        "airport": "机场管控区",
    }

    for ex in REAL_EXCLUSIONS:
        name = ex["name"]
        if name in seen_names:
            continue
        seen_names.add(name)
        ztype = ex["type"]
        lat = ex["lat"]
        lng = ex["lng"]
        zid = make_id(name, lat, lng)

        zones.append({
            "id": zid,
            "name": name,
            "zone_type": ztype,
            "zone_type_display": type_display_map.get(ztype, "未知"),
            "center_lat": lat,
            "center_lng": lng,
            "radius_km": ex["radius"],
            "description": f"福州市{name}禁区",
            "boundary": None,
            "source": "amap-v3",
            "typecode": "",
        })

    print(f"Generated {len(zones)} exclusion zones")
    return zones


