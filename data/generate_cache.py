def rand_point(district_name):
    """在指定区域内生成随机坐标"""
    d = DISTRICTS[district_name]
    lat = d["center"][0] + random.uniform(-d["radius"], d["radius"])
    lng = d["center"][1] + random.uniform(-d["radius"], d["radius"])
    return round(lat, 6), round(lng, 6)

def make_id(name, lat, lng):
    """生成稳定的ID"""
    return hashlib.md5(f"{name}{lat}{lng}".encode()).hexdigest()[:16]

