def generate_pois():
    """生成682+条POI数据，核心POI使用真实坐标，其余在对应区域内随机生成"""
    all_pois = []
    seen_ids = set()

    for fe_cat, info in REAL_POIS.items():
        typecode = info["typecode"]
        label = info["label"]
        max_count = 60

        # 1) 先添加真实坐标的POI
        for item in info.get("real", []):
            name, lat, lng, district = item
            poi_id = make_id(name, lat, lng)
            if poi_id in seen_ids:
                continue
            seen_ids.add(poi_id)
            flow, score = get_flow_and_score(fe_cat, name, district)
            all_pois.append({
                "id": poi_id,
                "name": name,
                "type": label,
                "typecode": typecode,
                "lat": lat,
                "lng": lng,
                "address": f"福州市{district}{name}",
                "adname": district,
                "cityname": "福州市",
                "category": fe_cat,
                "category_display": label,
                "district": district,
                "daily_flow": flow,
                "ev_demand_score": score,
                "influence_weight": round(score / 10.0 * 3.0, 1),
            })

        # 2) 用extra_names补充到max_count
        current_count = sum(1 for p in all_pois if p["category"] == fe_cat)
        extra_names = info.get("extra_names", [])
        for name in extra_names:
            if current_count >= max_count:
                break
            # 根据名称中的区域信息选择位置
            district = random.choice(MAIN_DISTRICTS)
            for d_name in ["鼓楼", "台江", "仓山", "晋安", "马尾", "长乐", "闽侯", "连江", "福清"]:
                if d_name in name:
                    for full_name in DISTRICTS:
                        if d_name in full_name:
                            district = full_name
                            break
                    break

            lat, lng = rand_point(district)
            poi_id = make_id(name, lat, lng)
            if poi_id in seen_ids:
                continue
            seen_ids.add(poi_id)
            flow, score = get_flow_and_score(fe_cat, name, district)
            all_pois.append({
                "id": poi_id,
                "name": name,
                "type": label,
                "typecode": typecode,
                "lat": lat,
                "lng": lng,
                "address": f"福州市{district}{name}",
                "adname": district,
                "cityname": "福州市",
                "category": fe_cat,
                "category_display": label,
                "district": district,
                "daily_flow": flow,
                "ev_demand_score": score,
                "influence_weight": round(score / 10.0 * 3.0, 1),
            })
            current_count += 1

    print(f"Generated {len(all_pois)} POIs")
    cat_counts = {}
    for p in all_pois:
        cat = p["category_display"]
        cat_counts[cat] = cat_counts.get(cat, 0) + 1
    for k, v in sorted(cat_counts.items(), key=lambda x: -x[1]):
        print(f"  {k}: {v}")
    return all_pois


