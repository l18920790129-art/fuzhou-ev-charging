def get_flow_and_score(fe_cat, name="", district=""):
    """根据类别和名称生成差异化的人流量和评分"""
    base_flow = DAILY_FLOW_BASE_MAP.get(fe_cat, 5000)
    base_score = EV_DEMAND_SCORE_MAP.get(fe_cat, 6.0)

    # 根据名称关键字调整（知名度高的POI给更高值）
    name_boost = 1.0
    if any(k in name for k in ["万达", "万象", "泰禾", "宝龙", "永辉", "苏宁"]):
        name_boost = 1.3
    elif any(k in name for k in ["东百", "融侨", "世欧", "正荣", "爱琴海"]):
        name_boost = 1.2
    elif any(k in name for k in ["省立", "协和", "附属第一", "福州大学", "福建师范"]):
        name_boost = 1.25
    elif any(k in name for k in ["东街口", "五四路", "火车站", "南站"]):
        name_boost = 1.35
    elif any(k in name for k in ["香格里拉", "洲际", "凯宾斯基", "希尔顿"]):
        name_boost = 1.2
    elif any(k in name for k in ["海底捞", "星巴克", "聚春园"]):
        name_boost = 1.15

    # 主城区比郊区人流量更高
    district_factor = 1.0
    if district in ["鼓楼区", "台江区"]:
        district_factor = 1.2
    elif district in ["仓山区", "晋安区"]:
        district_factor = 1.0
    elif district in ["马尾区", "长乐区"]:
        district_factor = 0.8
    else:
        district_factor = 0.7

    # 加入随机波动（±15%）
    jitter = random.uniform(0.85, 1.15)
    flow = int(base_flow * name_boost * district_factor * jitter)
    score = round(min(9.8, base_score * (name_boost ** 0.3) * random.uniform(0.95, 1.05)), 1)

    return flow, score


