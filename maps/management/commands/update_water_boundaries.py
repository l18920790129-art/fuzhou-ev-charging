class Command(BaseCommand):
    help = '为主要河道/水域禁区更新精确多边形边界'

    def handle(self, *args, **options):
        updated = 0
        skipped = 0
        for zone in ExclusionZone.objects.filter(zone_type='water'):
            polygon = WATER_POLYGONS.get(zone.name)
            if polygon:
                zone.boundary_json = json.dumps(polygon, ensure_ascii=False)
                zone.save(update_fields=['boundary_json'])
                self.stdout.write(self.style.SUCCESS(f'  ✅ 更新: {zone.name}'))
                updated += 1
            else:
                skipped += 1

        self.stdout.write(self.style.SUCCESS(
            f'\n完成！更新 {updated} 个水域多边形边界，跳过 {skipped} 个（无预设多边形，保留圆形约束）'
        ))
